def write_markdown(out_path: str):
    by_module = OrderedDict()
    for tc in TC:
        by_module.setdefault(tc["module"], []).append(tc)

    counts = Counter(tc["priority"] for tc in TC)
    by_type = Counter(tc["type"] for tc in TC)

    with open(out_path, "w", encoding="utf-8") as f:
        f.write("# vaberp ERP — Consolidated Test Cases\n\n")
        f.write("> **Source of truth:** `docs/build_test_cases.py` (regenerate by running it).\n")
        f.write("> **Sister formats:** `TEST_CASES.xlsx` (one sheet per module).\n")
        f.write("> **Strategy & conventions:** `docs/tests/00-test-strategy.md`.\n\n")

        # Summary
        f.write("## Summary\n\n")
        f.write(f"- **Total test cases:** {len(TC)}\n")
        f.write(f"- **Modules covered:** {len(by_module)}\n")
        f.write(f"- **By priority:** P0={counts.get('P0',0)} · P1={counts.get('P1',0)} · P2={counts.get('P2',0)} · P3={counts.get('P3',0)}\n")
        f.write(f"- **By type:** " + " · ".join(f"{k}={v}" for k, v in sorted(by_type.items())) + "\n\n")

        # Module index
        f.write("## Module index\n\n")
        f.write("| # | Module | TCs | P0 | P1 | P2 | P3 |\n|---|---|---|---|---|---|---|\n")
        for i, (mod, items) in enumerate(by_module.items(), 1):
            c = Counter(tc["priority"] for tc in items)
            anchor = mod.lower().replace(" ", "-").replace("&", "").replace("--", "-").replace("(", "").replace(")", "")
            f.write(f"| {i:02d} | [{mod}](#{anchor}) | {len(items)} | {c.get('P0',0)} | {c.get('P1',0)} | {c.get('P2',0)} | {c.get('P3',0)} |\n")
        f.write("\n---\n\n")

        # Table per module
        for mod, items in by_module.items():
            f.write(f"## {mod}\n\n")
            f.write(f"_{len(items)} test case{'s' if len(items)!=1 else ''}_\n\n")
            f.write("| TC ID | Submodule | Title | Type | Priority | Preconditions | Test Data | Steps | Expected Result | API Contract | Notes |\n")
            f.write("|---|---|---|---|---|---|---|---|---|---|---|\n")
            for tc in items:
                row = []
                for k in ("tc_id","submodule","title","type","priority","precond","data","steps","expected","api","notes"):
                    val = str(tc.get(k, "-")).replace("\r", "").replace("|", "\\|")
                    val = val.replace("\n", "<br/>")
                    row.append(val)
                f.write("| " + " | ".join(row) + " |\n")
            f.write("\n")

    return len(TC), len(by_module)


def write_excel(out_path: str):
    wb = Workbook()
    # Summary sheet first
    summary = wb.active
    summary.title = "Summary"

    by_module = OrderedDict()
    for tc in TC:
        by_module.setdefault(tc["module"], []).append(tc)

    counts = Counter(tc["priority"] for tc in TC)
    by_type = Counter(tc["type"] for tc in TC)

    bold = Font(bold=True, color="FFFFFF")
    header_fill = PatternFill("solid", fgColor="1F2937")
    soft = PatternFill("solid", fgColor="F3F4F6")
    border = Border(
        left=Side(style="thin", color="D1D5DB"),
        right=Side(style="thin", color="D1D5DB"),
        top=Side(style="thin", color="D1D5DB"),
        bottom=Side(style="thin", color="D1D5DB"),
    )
    wrap = Alignment(wrap_text=True, vertical="top")
    center = Alignment(horizontal="center", vertical="center")

    # ---- Summary contents
    summary["A1"] = "vaberp ERP — Test Cases Summary"
    summary["A1"].font = Font(bold=True, size=14)
    summary["A3"] = "Total test cases"
    summary["B3"] = len(TC)
    summary["A4"] = "Modules"
    summary["B4"] = len(by_module)

    summary["A6"] = "By Priority"
    summary["A6"].font = Font(bold=True)
    summary["A7"], summary["B7"] = "P0", counts.get("P0", 0)
    summary["A8"], summary["B8"] = "P1", counts.get("P1", 0)
    summary["A9"], summary["B9"] = "P2", counts.get("P2", 0)
    summary["A10"], summary["B10"] = "P3", counts.get("P3", 0)

    summary["D6"] = "By Type"
    summary["D6"].font = Font(bold=True)
    for i, (k, v) in enumerate(sorted(by_type.items())):
        summary.cell(row=7 + i, column=4, value=k)
        summary.cell(row=7 + i, column=5, value=v)

    # Module index on summary sheet
    start = 13
    summary.cell(row=start, column=1, value="Module").font = Font(bold=True)
    summary.cell(row=start, column=2, value="TCs").font = Font(bold=True)
    summary.cell(row=start, column=3, value="P0").font = Font(bold=True)
    summary.cell(row=start, column=4, value="P1").font = Font(bold=True)
    summary.cell(row=start, column=5, value="P2").font = Font(bold=True)
    summary.cell(row=start, column=6, value="P3").font = Font(bold=True)
    for i, (mod, items) in enumerate(by_module.items(), 1):
        c = Counter(tc["priority"] for tc in items)
        summary.cell(row=start + i, column=1, value=mod)
        summary.cell(row=start + i, column=2, value=len(items))
        summary.cell(row=start + i, column=3, value=c.get("P0", 0))
        summary.cell(row=start + i, column=4, value=c.get("P1", 0))
        summary.cell(row=start + i, column=5, value=c.get("P2", 0))
        summary.cell(row=start + i, column=6, value=c.get("P3", 0))
    for col_letter, width in zip("ABCDEF", [38, 8, 6, 6, 6, 6]):
        summary.column_dimensions[col_letter].width = width

    # ---- One sheet per module
    headers = [label for _, label in COLS]
    keys = [k for k, _ in COLS]

    PRIORITY_COLOR = {
        "P0": "FCA5A5",  # red-300
        "P1": "FCD34D",  # amber-300
        "P2": "BFDBFE",  # blue-200
        "P3": "D1FAE5",  # green-200
    }

    used_titles = set(["Summary"])
    for mod, items in by_module.items():
        # Excel sheet title limit = 31 chars; strip invalid chars
        title = mod.replace(":", "-").replace("\\", "-").replace("/", "-")[:31]
        base = title
        n = 1
        while title in used_titles:
            n += 1
            title = (base[: 31 - len(str(n)) - 1] + " " + str(n)).strip()
        used_titles.add(title)

        ws = wb.create_sheet(title=title)
        # Header row
        for col, h in enumerate(headers, 1):
            c = ws.cell(row=1, column=col, value=h)
            c.font = bold
            c.fill = header_fill
            c.alignment = center
            c.border = border
        # Data rows
        for r, tc in enumerate(items, 2):
            for col, k in enumerate(keys, 1):
                val = tc.get(k, "")
                c = ws.cell(row=r, column=col, value=val)
                c.alignment = wrap
                c.border = border
                if r % 2 == 0:
                    c.fill = soft
                if k == "priority" and val in PRIORITY_COLOR:
                    c.fill = PatternFill("solid", fgColor=PRIORITY_COLOR[val])
                    c.alignment = Alignment(horizontal="center", vertical="center")
        # Column widths
        widths = [18, 16, 18, 38, 14, 10, 30, 30, 38, 38, 32, 24]
        for col, w in enumerate(widths, 1):
            ws.column_dimensions[get_column_letter(col)].width = w
        ws.freeze_panes = "A2"
        ws.row_dimensions[1].height = 22

    wb.save(out_path)


def main():
    out_dir = os.path.dirname(os.path.abspath(__file__))
    md_path = os.path.join(out_dir, "TEST_CASES.md")
    xlsx_path = os.path.join(out_dir, "TEST_CASES.xlsx")
    n, mods = write_markdown(md_path)
    write_excel(xlsx_path)
    print(f"Wrote {md_path} ({n} test cases across {mods} modules)")
    print(f"Wrote {xlsx_path}")


if __name__ == "__main__":
    main()
